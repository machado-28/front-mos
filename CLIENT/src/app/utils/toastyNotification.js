import { Bounce, toast } from "react-toastify";

export function NotifyError(message = "Erro ao realizar a operação") {
  toast.error(message, {
    position: "top-right",
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "colored",
    transition: Bounce
  });
}

